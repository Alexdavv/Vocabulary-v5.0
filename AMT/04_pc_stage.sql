UPDATE pc_0_3
SET amount=
        CASE
            WHEN concept_name LIKE '%Day%' AND amount IS NULL
                THEN '16'
            WHEN concept_name LIKE '%Night%' AND amount IS NULL
                THEN '8'
            WHEN pack_name LIKE '%Ezetimibe%' AND amount IS NULL
                THEN '30'
            WHEN pack_name LIKE '%24%4%28%' AND amount IS NULL
                THEN '24'
            WHEN pack_name LIKE '%21%7%28%' AND amount IS NULL
                THEN '21'
            WHEN concept_name LIKE '%Amox%' OR concept_name LIKE '%Nexium%' AND amount IS NULL
                THEN '28'
            WHEN concept_name LIKE '%Ziprasidone%' AND amount IS NULL
                THEN '2'
            WHEN concept_name LIKE '%Peginterferon Alfa-2a%' OR concept_name LIKE '%Misoprostol%' AND amount IS NULL
                THEN '4'
            WHEN concept_name LIKE '%Esomeprazole%' OR concept_name LIKE '%Clarithromycin%' AND amount IS NULL
                THEN '14'
            WHEN concept_name LIKE '%Bisacodyl%' OR concept_name LIKE '%Quinagolide%' AND amount IS NULL
                THEN '3'
            WHEN concept_name LIKE '%Solution%' OR concept_name LIKE '%Cream%' OR concept_name LIKE '%Implant%' OR
                 concept_name LIKE '%Mifepristone%'
                OR concept_name LIKE '%Pessary%' OR concept_name LIKE '%Liquid%' OR concept_name LIKE '%Ointment%' OR
                 concept_name LIKE '%Vial%' OR concept_name LIKE '%Vagiclear%' OR amount = '0'
                THEN '1'
            WHEN concept_name LIKE '%Rebetol%'
                THEN substring(pack_name, '(\d+)\sX\sRebetol')
            WHEN concept_name LIKE '%Peg-Intron%'
                THEN substring(pack_name, '(\d+)\sX\sPeg-Intron')
            WHEN concept_name LIKE '%Bonecal%'
                THEN substring(pack_name, '(\d+)\sX\sBonecal')
            WHEN pack_name LIKE '% X %Calcium%' AND concept_name LIKE '%Calcium%'
                THEN substring(pack_name, '(\d+)\sX\sCalcium')
--when pack_name like '% X %Calcium%[%]%' and concept_name like '%Calcium%' then substring(substring (pack_name,'(Calcium.*\[\d\])'),'\d+') --doesn't match anything..
            WHEN pack_name LIKE '%Ribavirin%[%]%' AND concept_name LIKE '%Ribavirin%'
                THEN substring(pack_name, '(?:\d+\sX\sRibavirin)|(?:Ribavirin.*\[(\d+)\])')
            WHEN pack_name LIKE '% X %Ribavirin%' AND concept_name LIKE '%Ribavirin%'
                THEN substring(pack_name, '(\d+)\sX\sRibavirin')
            WHEN concept_name LIKE '%Viekira%'
                THEN '112'
            ELSE amount END;


