DROP TABLE IF EXISTS pc_0_1_1;
CREATE TABLE pc_0_1_1 AS
SELECT pack_code, pack_name, concept_name, concept_code,
       unnest(regexp_matches(substring(PACK_NAME, '(\[(\d)+.*\])'), '[^&]+', 'g')) AS pack_comp
FROM pc_0 t
WHERE pack_name LIKE '%(&)%'
--unnest for NULL (if regexp_matches dosn't catch any string) doesn't return ANY string. so adding manually
UNION
SELECT pack_code, pack_name, concept_name, concept_code, NULL AS pack_comp
FROM pc_0 t
WHERE pack_name LIKE '%(&)%';

DROP TABLE IF EXISTS pc_0_1;
CREATE TABLE pc_0_1 AS --(drugs are separeted by ,)
SELECT pack_code, pack_name, concept_name, concept_code,
       trim(unnest(regexp_matches(substring(PACK_NAME, '\(.*[0-9].*\)'), '[^,]+', 'g'))) AS pack_comp
FROM pc_0 t
WHERE pack_code NOT IN (SELECT pack_code FROM pc_0_1_1)
--same as above
UNION
SELECT pack_code, pack_name, concept_name, concept_code, NULL AS pack_comp
FROM pc_0 t
WHERE pack_code NOT IN (SELECT pack_code FROM pc_0_1_1);

