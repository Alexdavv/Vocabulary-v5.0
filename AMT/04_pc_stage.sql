DROP TABLE IF EXISTS pc_0;
CREATE TABLE pc_0 AS
SELECT DISTINCT a.concept_code AS pack_code, a.concept_name AS pack_name, c.concept_name, c.concept_code,
                c.concept_Class_id, c.SOURCE_CONCEPT_CLASS_ID
FROM drug_concept_stage a
     JOIN sources.amt_sct2_rela_full_au b
     ON a.concept_code = b.sourceid::text
     JOIN drug_concept_stage c
     ON c.concept_code = b.destinationid::text
WHERE a.concept_name LIKE '%[Drug Pack]'
  AND c.concept_class_id = 'Drug Product'
  AND typeid != '116680003'
  AND c.source_concept_class_id IN ('Trade Product Unit', 'Med Product Unit');--M Prod Pack and Cont Tr Prod Pack need to be excluded
;
